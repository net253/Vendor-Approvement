                     ยกเว้นในกรณีที่อุปกรณ์/ระบบเกิดความขัดข้อง และ/หรือต้องทำการซ่อมบำรุง
                     <br />
                     2.4 เราจะจัดวางป้ายตามความเหมาะสมในสถานที่ที่มีการใช้งานระบบ CCTV
