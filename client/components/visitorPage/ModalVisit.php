                  <p class="font-thai">
                     <b class="font-thai">7.การละเว้นการใช้สิทธิ</b><br />
                     การละเว้นไม่ใช้สิทธิ และ/หรือการผ่อนผันการใช้สิทธิไม่ว่าส่วนหนึ่งส่วนใดของเรา
                     ไม่ถือว่าเราสละสิทธิตามข้อตกลงหรือเงื่อนไขข้อนั้น ๆ
                     เรายังคงสงวนสิทธิที่จะเลือกใช้สิทธิตามข้อดังกล่าวได้ต่อไป
                  </p>
