               <div class="col-12">
                  <p class="font-thai">
                     <b class="font-thai">6. ข้อสงวนสิทธิ</b><br />
                     6.1 ท่านรับรู้ว่าประสิทธิภาพในการใช้งานเว็บไซต์ขึ้นอยู่กับประสิทธิภาพของอินเทอร์เน็ต
                     และสภาพอุปกรณ์ของท่านที่ใช้ในการเข้าถึงเว็บไซต์ด้วย
                     <br />6.2 ท่านยอมรับและเข้าใจว่าข้อมูลใด ๆ ที่ท่านได้รับจากการใช้เว็บไซต์นี้อาจไม่เหมาะสม แม่นยำ
                     สมบูรณ์ และ/หรือ เชื่อถือได้ และเราไม่มีหน้าที่รับผิดชอบใด ๆ ในความสูญเสีย เสียหาย หรือค่าใช้จ่ายใด
                     ๆ ที่เกิดขึ้นจากการที่ท่านใช้ข้อมูลที่ได้รับจากเว็บไซต์นี้
                     <br />6.3 ในการใช้งานเว็บไซต์หรือกรณีที่ท่านดาวน์โหลดข้อมูลและส่วนประกอบใด ๆ จากเว็บไซต์
                     ข้อมูลและส่วนประกอบดังกล่าวอาจไม่ถูกต้องสมบูรณ์ เพียงพอ เป็นปัจจุบัน เหมาะสม เชื่อถือได้
                     หรือปราศจากไวรัส, SPYWARE, MALWARE, COMPUTER WORM, TROJAN การเข้าเว็บไซต์นี้อาจจะไม่ต่อเนื่อง
                     มีความผิดพลาด ล่าช้า ไม่ถูกต้องครบถ้วน หรือไม่สามารถเข้าถึงระบบหรือข้อมูลในเว็บไซต์นี้ได้
                     ท่านตกลงรับความเสี่ยงภัยในความสูญหาย เสียหาย ค่าใช้จ่ายที่เกิดขึ้นต่อระบบคอมพิวเตอร์ สื่อกลาง
                     หรือเครื่องมือสื่อสาร หรือเครื่องมือและอุปกรณ์ใด ๆ ที่ใช้ในการเข้าถึงเว็บไซต์นี้
                     รวมตลอดถึงการสูญหายของข้อมูล
                     รวมทั้งความเสียหายโดยอ้อมซึ่งเป็นผลมาจากการใช้งานเว็บไซต์หรือดาวน์โหลดข้อมูลและส่วนประกอบจากเว็บไซต์
                     และท่านตกลงที่จะไม่เรียกร้องให้เรารับผิดใด ๆ
                     ในความขัดข้องของเว็บไซต์ที่ไม่ได้เกิดจากการกระทำโดยจงใจของเรา
                  </p>
