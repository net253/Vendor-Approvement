               <div class="col-12">
                  <p class="font-thai">
                     <b class="font-thai">3. ข้อมูลส่วนบุคคลและการใช้ข้อมูล</b><br />
                     ในการให้บริการตามวัตถุประสงค์ของเว็บไซต์ เราอาจมีการเก็บรวบรวม ใช้
                     และในบางกรณีอาจเปิดเผยข้อมูลของท่านเพื่อการให้บริการแก่ท่านหรือเพื่อประโยชน์ในการบริการของเราหรือบริษัทอื่นที่ดำเนินธุรกิจร่วมกับเรา
                     อย่างไรก็ดี เราให้ความสำคัญกับสิทธิความเป็นส่วนตัวของท่าน ดังนั้น
                     ในกรณีที่ข้อมูลนั้นเป็นข้อมูลส่วนบุคคลของท่าน
                     เราจะดำเนินการตามที่กำหนดในข้อนี้ต่อเมื่อได้รับความยินยอมจากท่านหรือเมื่อเราสามารถทำได้ตามกฎหมายเท่านั้น
                     ทั้งนี้ รายละเอียดต่าง ๆ เกี่ยวกับการเก็บรวบรวม ใช้ เปิดเผย
                     และประมวลข้อมูลส่วนบุคคลเป็นไปตามนโยบายความเป็นส่วนตัว
                     ซึ่งต้องให้ความยินยอมแยกต่างหากจากข้อกำหนดฉบับนี้
                     โดยท่านสามารถดูรายละเอียดเกี่ยวกับการประมวลผลข้อมูลส่วนบุคคลได้ที่นโยบายความเป็นส่วนตัวของเรา
                  </p>
