                  <p class="font-thai">
                     <b class="font-thai">2. เหตุใดเราจึงเก็บรวบรวม ใช้ หรือเปิดเผยข้อมูลส่วนบุคคลของท่าน</b><br />
                     2.1 เราอาจทำการรวบรวม เก็บภาพ ใช้ เปิดเผย โอน และดำเนินการใดๆ
                     ซึ่งรวมถึงแต่ไม่จำกัดเพียงการบันทึกการเก็บบันทึก การปรับเปลี่ยน การแก้ไข การดัดแปลง การทำลาย การลบ
                     การกู้คืน การรวม การทำสำเนา การส่งผ่าน การเก็บรักษา การถอน การปรับปรุง การเพิ่มเติม ต่อข้อมูลจาก
