               <div class="col-12">
                  <p class="font-thai">
                     <b class="font-thai">4. การปรับปรุงแก้ไข เปลี่ยนแปลง หรือหยุดการให้บริการเว็บไซต์</b><br />
                     ท่านเข้าใจและยอมรับว่า เรามีสิทธิที่จะปรับปรุง แก้ไข เปลี่ยนแปลง หรือหยุดการให้บริการส่วนใด ๆ
                     ของเว็บไซต์ ไม่ว่าจะเป็นรูปแบบการให้บริการ ระบบการทำงาน การแสดงผล และหรือข้อมูลใด ๆ
                     ที่ปรากฏอยู่บนเว็บไซต์ การที่ท่านตกลงดาวน์โหลด หรือใช้บริการเว็บไซต์หลังจากที่เราได้ทำการปรับปรุง
                     แก้ไข หรือเปลี่ยนแปลงแล้ว ถือว่าท่านได้ให้ความยินยอมโดยชัดแจ้งแก่เราในการติดตั้ง อัปเกรดและ/หรือ
                     อัปเดต การปรับปรุง แก้ไข เปลี่ยนแปลง หรือหยุดการให้บริการดังกล่าว
                  </p>
               </div>
               <div class="col-12">
                  <p class="font-thai">
                     <b class="font-thai">5. การปรับปรุงแก้ไข หรือเปลี่ยนแปลงข้อกำหนดและเงื่อนไข</b><br />
                     5.1 ท่านเข้าใจและยอมรับว่า เรามีสิทธิที่จะปรับปรุงแก้ไข
                     หรือเปลี่ยนแปลงข้อกำหนดและเงื่อนไขนี้โดยไม่แจ้งล่วงหน้า
                     โดยเราจะประกาศข้อกำหนดและเงื่อนไขที่ปรับปรุงแก้ไขหรือเปลี่ยนแปลงบนเว็บไซต์ให้ท่านได้พิจารณาก่อนการเข้าใช้งานเว็บไซต์
                     พร้อมระบุวันที่แก้ไขเพิ่มเติมครั้งล่าสุด
                     การที่ท่านใช้บริการทางเว็บไซต์ของเราต่อไปหลังจากที่มีการปรับปรุงแก้ไข
                     หรือเปลี่ยนแปลงข้อกำหนดและเงื่อนไขแล้ว จะถือว่าท่านยอมรับข้อกำหนดและเงื่อนไขที่ปรับปรุงแก้ไข
                     หรือเปลี่ยนแปลงนั้นแล้วโดยชัดแจ้ง
                  </p>
