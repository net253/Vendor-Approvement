                  <p class="font-thai">
                     <b class="font-thai">3. บุคคลใดที่อาจได้รับการเปิดเผยข้อมูลส่วนบุคคลของท่าน</b><br />
                     3.1 เราจะเก็บข้อมูลส่วนบุคคลที่เกี่ยวข้องกับท่านในระบบ CCTV ไว้เป็นความลับ
                     และจะทำการเปิดเผยหรือถ่ายโอนข้อมูลส่วนบุคคลดังกล่าวเฉพาะเพียงบริษัทลูก บริษัทในเครือของเรา
                     บุคคลภายนอกอื่นๆ ที่จะถูกคัดเลือกอย่างระมัดระวังในเวลานี้หรือในอนาคต ผู้ได้รับอนุญาต
                     พันธมิตรกิจการร่วมค้า และ/หรือผู้ให้บริการ (ซึ่งอาจตั้งอยู่ในต่างประเทศ)
                     เพื่อให้บรรลุวัตถุประสงค์การตรวจสอบโดย CCTV ตามที่ระบุไว้ในนโยบายนี้
                     <br />
                     3.2 บุคคลภายนอกซึ่งเราอาจเปิดเผยข้อมูลจากระบบ CCTV และข้อมูลส่วนบุคคลอื่นๆ ที่เกี่ยวข้องกับท่าน
                     ซึ่งรวมถึงการเปิดเผยต่อบริษัทในเครือของเรา
                     (เป็นส่วนหนึ่งของประโยชน์โดยชอบด้วยกฎหมายของเราและประโยชน์ของบริษัทในเครือของเรา
                     เพื่อให้บรรลุวัตถุประสงค์การตรวจสอบโดย CCTV) หน่วยงานภาครัฐ และ/หรือองค์กรที่ทำหน้าที่กำกับดูแล
                     (เพื่อปฏิบัติตามหน้าที่ตามกฎหมาย
                     หรือเพื่อสนับสนุนหรือช่วยเหลือแก่องค์กรบังคับใช้กฎหมายในเรื่องการสอบสวนและดำเนินคดีทางแพ่งหรือทางอาญา)
