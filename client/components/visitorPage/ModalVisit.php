                  <p class="font-thai">
                     <b class="font-thai">1. สิทธิของเรา</b><br />
                     1.1 เราเป็นเจ้าของข้อมูลและลิขสิทธิ์ เครื่องหมายการค้า สัญลักษณ์ เครื่องหมายบริการ และชื่อทางการค้า
                     ไม่ว่าจดทะเบียนหรือไม่ได้จดทะเบียน ที่ปรากฏอยู่ในเว็บไซต์นี้ และส่วนประกอบอื่น ๆ ของเว็บไซต์
                     เราไม่อนุญาตหรือยินยอมให้มีการใช้ข้อมูลและลิขสิทธิ์ เครื่องหมายการค้า สัญลักษณ์ เครื่องหมายบริการ
                     และชื่อทางการค้าดังกล่าวโดยไม่ได้รับความยินยอมเป็นลายลักษณ์อักษรจากเรา
                     หรือตามที่กำหนดไว้ในเว็บไซต์นี้
                     <br />1.2
                     เราเป็นเจ้าของและเป็นผู้มีสิทธิแต่เพียงผู้เดียวในข้อมูลทั้งหมดที่ปรากฏบนเว็บไซต์นี้ไม่ว่าจะอยู่ในรูปแบบใด
                     เว้นแต่จะกำหนดเป็นอย่างอื่นในข้อกำหนดและเงื่อนไขการใช้เว็บไซต์หรือเอกสารอื่น ๆ ที่เกี่ยวข้อง
                     ข้อมูลทั้งหมดได้รับความคุ้มครองตามกฎหมาย ท่านตกลงจะไม่คัดลอก ดาวน์โหลด สั่งพิมพ์ และ/หรือ
                     ใช้ข้อมูลในเว็บไซต์นี้เว้นแต่ได้รับอนุญาตจากเราโดยชัดแจ้ง ทั้งนี้
                     ข้อมูลส่วนบุคคลของท่านไม่อยู่ในบังคับของข้อนี้และจะใช้บังคับตามนโยบายความเป็นส่วนตัว
                     <br />1.3 ท่านตกลงจะไม่ ทำซ้ำ ดัดแปลง ปรับปรุง แก้ไข ต่อเติม เรียบเรียงใหม่ หรือวิศวกรรมย้อนกลับ
                     (Reverse Engineering) เว็บไซต์ ไม่ว่ากรณีใด ๆ ก็ตาม และจะไม่ลบ ทำลาย ทำให้เสียหาย
                     หรือทำให้ไม่ชัดเจนซึ่งเครื่องหมายหรือสัญลักษณ์แสดงความเป็นเจ้าของลิขสิทธิ์หรือเครื่องหมายการค้าหรือบริการของบริษัทไม่ว่าจะโดยจงใจหรือประมาทเลินเล่อก็ตาม
                     ทั้งนี้ ท่านตกลงจะไม่แต่งตั้งให้บุคคลภายนอกกระทำการตามข้อนี้
                     <br />1.4 ข้อมูลที่ปรากฏในเว็บไซต์นี้ อาจไม่ถูกต้อง ไม่สมบูรณ์
                     หรือไม่ตรงตามข้อเท็จจริงหรือไม่เป็นปัจจุบัน ท่านไม่ควรตัดสินใจดำเนินการใด ๆ
                     โดยยึดถือตามข้อมูลที่ปรากฏในเว็บไซต์นี้แต่เพียงอย่างเดียว
                     การใช้ข้อมูลในเว็บไซต์นี้เป็นความเสี่ยงของตัวท่านเอง โดยเราจะไม่รับผิดชอบใด ๆ ในความสูญเสีย เสียหาย
                     หรือค่าใช้จ่ายใด ๆ ที่เกิดขึ้นจากการที่ท่านใช้ข้อมูลที่ได้รับจากเว็บไซต์นี้ และ
                     เรามีสิทธิที่จะปรับปรุง เปลี่ยนแปลงข้อมูลที่อยู่ในเว็บไซต์ได้ตลอดเวลาโดยเป็นดุลพินิจของ
                     เราแต่เพียงผู้เดียว
                     <br />1.5 ท่านตกลงที่จะไม่รบกวนการทำงานของเว็บไซต์ไม่ว่าด้วยวิธีการใด รวมถึงจะไม่กระทำการใด ๆ
                     ที่ขัดหรือแย้งกับนโยบายข้อกำหนดและเงื่อนไขของเรา
                     ไม่กระทำผิดกฎหมายหรือทำสิ่งใดที่ขัดต่อความสงบเรียบร้อยหรือศีลธรรมอันดีของประชาชน
                     เราสงวนสิทธิที่จะจำกัดหรือระงับการใช้งานของท่านในเวลาใด ๆ ไม่ว่าทั้งหมดหรือบางส่วน
                     รวมถึงการกระทำอื่นใด ๆ เพื่อหยุดการกระทำของท่านโดยไม่ต้องแจ้งให้ทราบล่วงหน้า
                     ทั้งนี้การระงับการใช้งานไม่ถือเป็นการสละสิทธิหรือมาตรการใด ๆ ที่ เรามีอยู่ตามกฎหมาย
                  </p>
