                  <p class="font-thai">
                     <b class="font-thai">2. ข้อมูลที่ท่านให้ไว้ในเว็บไซต์</b><br />
                     ท่านรับรองและรับประกันว่าท่านเป็นเจ้าของหรือเป็นผู้มีสิทธิในข้อมูลที่ตนเองให้ไว้
                     และข้อมูลเหล่านั้นสมบูรณ์ ถูกต้อง ตรงความเป็นจริง มีความเหมาะสม ชอบด้วยกฎหมาย
                     ไม่ละเมิดข้อกำหนดการใช้งานใด ๆ ในเว็บไซต์นี้ รวมถึงไม่เป็นการแอบอ้างเป็นบุคคลอื่น
                     และจะไม่ก่อให้เกิดความเสียหายต่อบุคคลใด ๆ
                     และท่านตกลงจะรับผิดในความเสียหายที่เกิดแก่เราและ/หรือบุคคลอื่นตามจำนวนที่เกิดขึ้นจริงในกรณีที่ท่านไม่ปฏิบัติตามข้อนี้ท่าน
                  </p>
