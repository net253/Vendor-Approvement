                     ตามแต่กรณี เราจะพยายามระบุถึงขั้นตอนที่จำเป็นเพื่อให้เกิดความสมดุลที่เหมาะตามแต่สมควร
                     <br />
                     2.2 เราจะติดตั้งอุปกรณ์ CCTV ที่จุดสำคัญภายในและโดยรอบสถานที่ อาคารและพื้นที่ต่างๆ ของเรา
                     ยกเว้นบางพื้นที่ เช่น ห้องสุขา
